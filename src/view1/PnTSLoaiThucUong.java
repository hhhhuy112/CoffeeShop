    public void setRowsColor() {
        ArrayList<Integer> alRowsMax10 = new ArrayList();
        ArrayList<Integer> alRowsMin10 = new ArrayList();
        ArrayList<Integer> alRowsActive = new ArrayList();

        rowCount = tbLoaiThucUong.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            int sl = (int) tbLoaiThucUong.getValueAt(i, 3);
            if (sl <= 10) {
                alRowsMax10.add(i);
            }
            if (sl > 10) {
                alRowsMin10.add(i);
            }

        }
        for (int j = 0; j < tbLoaiThucUong.getColumnCount(); j++) {
            tbLoaiThucUong.getColumnModel().getColumn(j).setCellRenderer(new TableCellRendererColor(alRowsMax10, alRowsMin10));
        }
        revalidate();
    }


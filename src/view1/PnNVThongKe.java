    public void createExcel(String filename) throws IOException, WriteException {
        WritableWorkbook workBook = Workbook.createWorkbook(new File(filename));
        WritableSheet sheet = workBook.createSheet("Thống kê", 0);
        Vector<Vector> vGetDetail = getDetailTB(tbHoaDon);
        for (int i = 0; i < tbHoaDon.getColumnCount(); i++) {
            sheet.addCell(new Label(i, 0, tbHoaDon.getColumnName(i)));
            sheet.addCell(new EmptyCell(2, 2));
        }
        for (int i = 0; i < vGetDetail.size(); i++) {
            for (int j = 0; j < vGetDetail.get(i).size(); j++) {
                sheet.addCell(new jxl.write.Label(j, i + 1, String.valueOf(vGetDetail.get(i).get(j))));
            }
        }

        workBook.write();
        workBook.close();
    }

    public Vector<Vector> getDetailTB(JTable tbHoaDon) {
        Vector<Vector> vAll = new Vector<>();
        for (int i = 0; i < tbHoaDon.getRowCount(); i++) {
            Vector v = new Vector();
            for (int j = 0; j < tbHoaDon.getColumnCount(); j++) {

                v.add(tbHoaDon.getValueAt(i, j));
            }
            vAll.add(v);
        }
        return vAll;
    }


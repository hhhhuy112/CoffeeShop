    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        FileNameExtensionFilter xmlFilter = new FileNameExtensionFilter("xls files (*.xls)", "xls");
        fc.addChoosableFileFilter(xmlFilter);
        fc.setFileFilter(xmlFilter);

        int ans = fc.showSaveDialog(null);
        if (ans == JFileChooser.APPROVE_OPTION) {
            SimpleDateFormat  sdf=new SimpleDateFormat("dd_MM_yyyy");
            String path = fc.getSelectedFile().getAbsolutePath() + File.separator + "KetQua"+sdf.format(new Date())+".xls";
            System.out.println(path);

            try {
                createExcel(path);
                JOptionPane.showMessageDialog(null,"Tạo file thành công");
            } catch (IOException ex) {
                Logger.getLogger(PnNVThongKe.class.getName()).log(Level.SEVERE, null, ex);
                  JOptionPane.showMessageDialog(null,"Tạo file thất bại");
            } catch (WriteException ex) {
                Logger.getLogger(PnNVThongKe.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"Tạo file thất bại");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed


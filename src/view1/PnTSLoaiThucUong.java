    private void btLTUTimKiemFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLTUTimKiemFindActionPerformed
        // TODO add your handling code here:
        LoaiThucUong ltu = new LoaiThucUong();
        int id = 0;
        if (!"".equals(tfLTUIdFind.getText())) {
            try {
                id = Integer.parseInt(tfLTUIdFind.getText());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập vào id là số");
            }

        }
        ltu.setId_ltu(id);
        //GIa
        double gS = -1;
        double gE = -1;
        if ("X".equals(btLTUGiaActiveFind.getText())) {
            if ((double) spinLTUGiaS.getValue() > (double) spinTUGiaE.getValue()) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn giá khởi điểm nhỏ hơn giá lớn nhất");
            } else {
                gE = (double) spinTUGiaE.getValue();
                gS = (double) spinLTUGiaS.getValue();
            }
        }
        ltu.setGiaMax_ltu(gE);
        ltu.setGia_ltu(gS);
        //Số lượng
        int sLS = -1;
        int sLE = -1;
        if ("X".equals(btLTUSoLuongActiveFind.getText())) {
            if ((int) spinTUSoLuongS.getValue() > (int) spinTUSoLuongE.getValue()) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn số lượng khởi điểm nhỏ hơn giá lớn nhất");
            } else {
                sLS = (int) spinTUSoLuongS.getValue();
                sLE = (int) spinTUSoLuongE.getValue();
            }
        }
        ltu.setSoLuongMax_ltu(sLE);
        ltu.setSoLuong_ltu(sLS);
        ltu.setTen_ltu(tfLTUNameFind.getText());
               
        boolean ck=false;
        if ("X".equals(btLTUTTActive.getText())) {
            ck = true;
            ltu.setTrangThai_ltu(ckBoxTTFind.isSelected());
        }
        loadTable(ltu, ck);
        System.out.println(ck);
        resetForm();
    }//GEN-LAST:event_btLTUTimKiemFindActionPerformed

